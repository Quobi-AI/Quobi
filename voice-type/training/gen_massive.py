def corrupt_lang(clean, fillers):
    t = re.sub(r'[.,!?;:"()¿¡]', " ", clean.lower())
    out = []
    if RNG.random() < 0.6:
        out.append(RNG.choice(fillers))
    for w in re.sub(r"\s+", " ", t).strip().split():
        if RNG.random() < 0.08:
            out.append(RNG.choice(fillers))
        out.append(w)
    return " ".join(out)

