# ------------------------------------------ TIDY at scale (verbosify)
# Insert REDUNDANT filler-words into a concise sentence -> verbose raw; the tidy
# target is the concise sentence (tidy = strip the redundancy, unlike verbatim
# which keeps every word). This is the actual verbatim-vs-tidy distinction.
_PAD = ["just", "really", "kind of", "sort of", "basically", "you know",
        "honestly", "actually", "i think", "i guess", "pretty much", "or whatever",
        "at this point", "to be honest", "i mean"]
def verbosify(concise: str) -> str:
    core = concise.rstrip(".!?").lower().replace("’", "'").replace("'", "")
    core = re.sub(r"[.,!?;:\"()]", " ", core)
    words = re.sub(r"\s+", " ", core).strip().split()
    out: list[str] = []
    if RNG.random() < 0.6:
        out.append(RNG.choice(["um", "uh", "so um", "yeah so", "okay so"]))
    for w in words:
        out.append(w)
        if RNG.random() < 0.15:
            out.append(RNG.choice(_PAD))
    return " ".join(out)

