# ---------------------------------------------------------------- corruption
_REMOVABLE = ["um", "uh", "er", "hmm", "like", "you know"]   # ONLY disfluencies verbatim removes
def corrupt(clean: str) -> str:
    """Clean, well-formed sentence -> realistic raw STT (lowercase, no punct/
    apostrophes, injected removable disfluencies + occasional stutter)."""
    t = clean.lower().replace("’", "'")
    t = re.sub(r"[.,!?;:\"()\[\]]", " ", t)
    t = t.replace("'", "")                  # cant, im, lets, thats ...
    t = re.sub(r"\s+", " ", t).strip()
    words = t.split()
    out: list[str] = []
    if RNG.random() < 0.55:                 # sometimes a leading filler
        out.append(RNG.choice(["um", "uh", "so um", "uh like", "okay um", "yeah um"][:4] + ["um", "uh"]))
    for w in words:
        if RNG.random() < 0.06:             # mid filler (tic)
            out.append(RNG.choice(_REMOVABLE))
        out.append(w)
        if RNG.random() < 0.025:            # stutter
            out.append(w)
    return " ".join(out)

def verbatim_pairs(cleans, per=2):
    """Each clean sentence -> `per` distinct corruptions (deduped)."""
    pairs = []
    for c in cleans:
        seen = set()
        tries = 0
        while len(seen) < per and tries < per * 4:
            tries += 1
            r = corrupt(c)
            if r and r not in seen:
                seen.add(r); pairs.append((r, c, "verbatim"))
    return pairs

# ------------------------------------------------- clean-sentence templates
def fill(templates, **pools):
    """Expand each template over random slot combos, capped per template."""
    out = []
    for tmpl, cap in templates:
        slots = re.findall(r"\{(\w+)\}", tmpl)
        seen = set()
        tries = 0
        while len(seen) < cap and tries < cap * 6:
            tries += 1
            vals = {s: RNG.choice(pools[s]) for s in slots}
            s = tmpl.format(**vals)
            if s not in seen:
                seen.add(s); out.append(s)
    return out

